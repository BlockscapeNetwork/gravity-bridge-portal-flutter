import 'package:flutter/material.dart';
import 'package:flutter_localizations/flutter_localizations.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';
import 'package:gravity_bridge_flutter/services/route_service.dart';
import 'package:gravity_bridge_flutter/services/themedata_service.dart';

import 'package:gravity_bridge_flutter/generated/l10n.dart';

class MyApp extends ConsumerStatefulWidget {
  const MyApp({Key? key}) : super(key: key);

  @override
  _MyAppState createState() => _MyAppState();
}

class _MyAppState extends ConsumerState<MyApp> {
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      theme: themeDataLight(),
      darkTheme: themeDataDark(),
      themeMode: ref.watch(themeModeProvider),
      title: 'Gravity Bridge Portal',
      // localization stuff
      // checkout Flutter Intl vscode plugin description
      localizationsDelegates: const [
        S.delegate,
        GlobalMaterialLocalizations.delegate,
        GlobalWidgetsLocalizations.delegate,
        GlobalCupertinoLocalizations.delegate,
      ],
      supportedLocales: S.delegate.supportedLocales,
      //RouteService provides info about existing routes
      // "/" is Flutters default first page/entry page
      routes: RouteService.routes,
    );
  }
}
